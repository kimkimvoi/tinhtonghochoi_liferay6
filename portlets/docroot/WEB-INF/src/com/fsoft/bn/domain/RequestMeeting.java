package com.fsoft.bn.domain;


public class RequestMeeting extends OperatorSteering{
	private String requestMeetingPlate;
	private String requestMeetingOperator;
	private String requestMeetingAttended;
	
	public String getRequestMeetingPlate() {
		return requestMeetingPlate;
	}
	public void setRequestMeetingPlate(String requestMeetingPlate) {
		this.requestMeetingPlate = requestMeetingPlate;
	}
	public String getRequestMeetingOperator() {
		return requestMeetingOperator;
	}
	public void setRequestMeetingOperator(String requestMeetingOperator) {
		this.requestMeetingOperator = requestMeetingOperator;
	}
	public String getRequestMeetingAttended() {
		return requestMeetingAttended;
	}
	public void setRequestMeetingAttended(String requestMeetingAttended) {
		this.requestMeetingAttended = requestMeetingAttended;
	}
	
	
}
